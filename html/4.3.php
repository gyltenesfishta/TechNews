<h3>Shefat e Big Tech përballen me ndjekje penale në projektligjin e ndryshuar të sigurisë në internet </h3><br>

<p>

    Drejtuesit e teknologjisë, platformat e të cilëve vazhdimisht dështojnë në mbrojtjen e fëmijëve nga dëmtimi në internet, mund të përballen me akuza penale, në një lëshim të qeverisë për deputetët rebelë konservatorë.<br><br><br>
    Administrata konservatore e kryeministrit Rishi Sunak ka premtuar të ndryshojë Projektligjin e Sigurisë në internet për të futur dënime me burg për menaxherët që nuk arrijnë të mbrojnë fëmijët nga dëmtimi në internet.
    Propozimi u nxit nga afro 50 deputetë konservatorë, me mbështetjen e Partisë së Punës.<br><br><br>
    Projektligji i shumë vonuar i Sigurisë në internet është paraqitur nga qeveria si një ligj novator që do të synojë racizmin në internet, abuzimin seksual, ngacmimin, mashtrimin dhe materiale të tjera të dëmshme që gjenden shpesh në internet.
    Në formën e tij origjinale, ligji u dha rregullatorëve kompetenca të gjera për të sanksionuar kompanitë e mediave dixhitale dhe sociale si Google, Facebook, Twitter dhe TikTok. Megjithatë, ai është zbutur ndjeshëm në rishikimet gjatë vitit të kaluar.<br><br><br>
    Sipas amendamentit të ri, qeveria pritet të synojë drejtuesit që injorojnë njoftimet e zbatimit nga Ofcom, rregullatori i komunikimit, në lidhje me shkeljet e detyrave të sigurisë së fëmijëve të tyre, të cilat përfshijnë mbrojtjen e fëmijëve nga përmbajtje të dëmshme, si materiale që promovojnë vetëlëndimin dhe çrregullimet e të ngrënit. <br><br><br>
    Megjithatë, ai nuk do të penalizojë drejtuesit që kanë “vepruar në mirëbesim për të përmbushur në mënyrë proporcionale” detyrat e tyre.
    Vendimi shënon herën e tretë që kryeministri Rishi Sunak tërhiqet përballë mbështetësve rebelë që nga marrja e pushtetit në tetor, pas lëshimeve për çështjet e parqeve me erë në tokë dhe objektivave të detyrueshme të strehimit për këshillat.<br><br><br>
    Pas negociatave të gjata gjatë fundjavës, Michelle Donelan, Sekretarja e Kulturës në Mbretërinë e Bashkuar, ka arritur tani një marrëveshje me rebelët, duke i lejuar Kryeministrit të shmangë humbjen e tij të parë të madhe në Commons.
    Në një deklaratë për deputetët, Donelan tha se qeveria do të amendojë Projektligjin e Sigurisë në internet në mënyrë që menaxherët e lartë që "kanë rënë dakord ose kanë pranuar të injorojnë kërkesat e detyrueshme, duke rrezikuar dëmtime serioze për fëmijët" të përballen me dënime penale.<br><br><br>
    “Dënimet penale, përfshirë burgimin dhe gjobat, do të jenë në përpjesëtim me vepra të ngjashme,” tha ajo. “Ndërsa ky amendament nuk do të ndikojë tek ata që kanë vepruar me mirëbesim që të respektojnë në mënyrë proporcionale, ai i jep aktit dhëmbë shtesë për të sjellë ndryshime dhe për të siguruar që njerëzit të mbajnë përgjegjësi nëse nuk arrijnë të mbrojnë siç duhet fëmijët.”<br><br><br>
   
   
    <img src="/images/media1.jpg" class="fotonemes" alt="Social Media icons"><br><br><br><br><br><br>

   
    Sipas propozimit aktual, drejtuesve të teknologjisë do t'u kërkohet të marrin "masa proporcionale" për të ndaluar fëmijët të shohin materiale të dëmshme, të tilla si përmes verifikimit të moshës, duke hequr përmbajtje të tillë dhe duke mundësuar kontrollin prindëror.
    Për momentin, projektligji do t'i bënte menaxherët vetëm penalisht përgjegjës për mosdhënien e informacionit te rregullatori i medias Ofcom, i cili do të fitojë kompetenca të gjera për të kontrolluar internetin sipas ligjit të ri.<br><br><br>
    Megjithëse fillimisht u propozua që menaxherët të jenë përgjegjës për mospërmbushjen e detyrave më të gjera të sigurisë, projektligji u refuzua pas një konsultimi përpara prezantimit të tij, i cili arriti në përfundimin se mund ta bënte sektorin e teknologjisë në Mbretërinë e Bashkuar më pak tërheqës për kompanitë dhe investitorët e huaj.
    Marrëveshja e fundit do të thotë se rebelët tani do të tërheqin amendamentin e tyre ndërsa qeveria rregullon propozimet e saj.<br><br><br>
    Një burim qeveritar tha se Donelan ishte "i kënaqur që kolegët nuk do t'i shtyjnë më amendamentet e tyre për votim pas bisedave dhe punës konstruktive".
    NSPCC, shoqata bamirëse për mbrojtjen e fëmijëve, tha se përgjegjësia e menaxhmentit të lartë do të ndihmonte në krijimin e ndryshimit të kulturës brenda firmave të teknologjisë.<br><br><br>
    “Duke u përkushtuar ndaj përgjegjësisë së menaxherit të lartë, sekretarja e kulturës ka dërguar një sinjal të fortë dhe të mirëpritur se ajo do t'i japë faturës së sigurisë në internet dhëmbët e nevojshëm për të nxitur një ndryshim kulture brenda zemrës së kompanive të teknologjisë që do të ndihmojnë në mbrojtjen e fëmijëve nga tragjeditë e ardhshme. tha Richard Collard, shefi i asociuar i politikës në internet të sigurisë së fëmijëve në NSPCC.<br><br><br>
    Sekretarja e kulturës në hije të Laburistëve, Lucy Powell tha se Ofcom kishte nevojë për "dhëmbë të mjaftueshëm që shefat e Silicon Valley të uleshin dhe të merrnin parasysh".
    Ian Russell, babai i nxënëses Molly Russell, e cila vrau veten pasi kishte parë materiale të dëmshme në mediat sociale, tha se kërcënimi me burgim është "e vetmja gjë" që do t'i bëjë bosët "të vënë sigurinë në krye të axhendës së tyre".<br><br><br>
    Duke folur për Newsnight të BBC-së, ai shtoi: "Unë mendoj se kjo është një gjë vërtet e rëndësishme për sa i përket ndryshimit të kulturës së korporatës në këto platforma".
    Megjithatë, njerëz të tillë si James Levy, partner për zgjidhjen e mosmarrëveshjeve në firmën ligjore Ashurst, kanë ngritur shqetësime nëse amendamenti do të ishte realisht në gjendje të parandalonte dëmin në internet.<br><br><br>
    "Ne parashikojmë që nëse një ligj i tillë miratohet, praktikisht nuk do të ketë asnjë ndikim praktik në përpjekjet e atyre firmave për të zhdukur dëmet në internet," tha ai. Ne sugjerojmë se qasja më e mirë është përmes inkurajimit të hapave praktikë brenda këtyre bizneseve me mbështetjen e akteve rregullatore të zbatimit.<br><br><br>



</p>



